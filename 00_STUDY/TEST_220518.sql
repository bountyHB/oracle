SELECT ROWNUM, EMP_NAME, SALARY
FROM EMPlOYEE
WHERE ROWNUM <= 3
ORDER BY SALARY DESC;

-- EMPLOYEE 테이블로 
SELECT ROWNUM, EMP_NAME, SALARY
FROM EMPlOYEE
