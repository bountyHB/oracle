----------------------------다중 JOIN 실습 문제-----------------------------
-- 1. 사번, 직원명, 부서명, 지역명, 국가명을 조회
-- ANSI
SELECT E.EMP_ID, E.EMP_NAME, D.DEPT_TITLE, L.LOCAL_NAME, N.NATIONAL_NAME
FROM EMPLOYEE E  
JOIN DEPARTMENT D ON (E.DEPT_CODE = D.DEPT_ID)
JOIN LOCATION L ON (D.LOCATION_ID = L.LOCAL_CODE)
JOIN NATIONAL N ON (L.NATIONAL_CODE = N.NATIONAL_CODE);

-- 오라클
SELECT E.EMP_ID, E.EMP_NAME, D.DEPT_TITLE, L.LOCAL_NAME, N.NATIONAL_NAME
FROM EMPLOYEE E, DEPARTMENT D, LOCATION L, NATIONAL N
WHERE (E.DEPT_CODE = D.DEPT_ID) AND (D.LOCATION_ID = L.LOCAL_CODE) 
      AND (L.NATIONAL_CODE = N.NATIONAL_CODE);
      

-- 2. 사번, 직원명, 부서명, 직급명, 지역명, 국가명, 급여 등급 조회
-- ANSI
SELECT E.EMP_ID, E.EMP_NAME, D.DEPT_TITLE, J.JOB_NAME, L.LOCAL_NAME, N.NATIONAL_NAME, S.SAL_LEVEL
FROM EMPLOYEE E
JOIN SAL_GRADE S ON (E.SALARY BETWEEN S.MIN_SAL AND S.MAX_SAL)
JOIN DEPARTMENT D ON (E.DEPT_CODE = D.DEPT_ID)
JOIN JOB J ON (E.JOB_CODE = J.JOB_CODE)
JOIN LOCATION L ON (D.LOCATION_ID = L.LOCAL_CODE)
JOIN NATIONAL N ON (L.NATIONAL_CODE = N.NATIONAL_CODE);

-- 오라클
SELECT E.EMP_ID, E.EMP_NAME, D.DEPT_TITLE, J.JOB_NAME, L.LOCAL_NAME, N.NATIONAL_NAME, S.SAL_LEVEL
FROM EMPLOYEE E, SAL_GRADE S, DEPARTMENT D, JOB J, LOCATION L, NATIONAL N
WHERE (E.SALARY BETWEEN S.MIN_SAL AND S.MAX_SAL)AND(E.DEPT_CODE = D.DEPT_ID)AND
(E.JOB_CODE = J.JOB_CODE)AND(D.LOCATION_ID = L.LOCAL_CODE)AND(L.NATIONAL_CODE = N.NATIONAL_CODE);

